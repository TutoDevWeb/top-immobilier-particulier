L'INDI, l'Institut Notarial De l'Immobilier.

NEXITY : Alain Dinin, le PDG du groupe. Leader Français de la promotion immobilière (Logement, Entreprise et Services), Nexity est présent sur l'ensemble du territoire national à travers ses filiales (Foncier Conseil, George V, Geprim, Nexity Services, Saggel, Nexity Entreprises ...). Le pôle logement, qui représente plus de 75 % de l'activité, conjugue les activités de promoteur et de lotisseur. Spécialiste des immeubles de grande hauteur, des immeubles neufs et des réhabilitations lourdes, Nexity Entreprises intervient dans les quartiers d'affaires en Ile de France et en régions. Enfin, Nexity Services a pour vocation d'accompagner l'actif immobilier dans son évaluation, son acquisition, sa gestion quotidienne, mais aussi sa restructuration ou sa cession.
Le groupe est en plein rapprochement avec les Caisses d'Epargne. La CNCE a apporté à Nexity environ 25% du Crédit Foncier (qui reste affilié à sa maison mère), 100% de GCE Immobilier et plus de 33% d'Eurosic. En contrepartie, la CNCE est rémunérée en nouveaux titres émis par Nexity, correspondant à une participation d'environ 39,9%. L'Ecureuil est ainsi devenu l'actionnaire de référence de Nexity, pôle immobilier du Groupe Caisse d'Epargne, mais qui garde une autonomie propre à assurer son développement auprès de ses clients et d'autres acteurs bancaires.


